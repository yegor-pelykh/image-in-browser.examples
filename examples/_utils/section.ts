/** @format */

/**
 * Enum representing different sections of image processing.
 */
export enum Section {
  /** Section for APNG images */
  apng,
  /** Section for BMP images */
  bmp,
  /** Section for color processing */
  color,
  /** Section for drawing operations */
  draw,
  /** Section for dumping image data */
  dump,
  /** Section for EXIF metadata */
  exif,
  /** Section for applying filters */
  filter,
  /** Section for GIF images */
  gif,
  /** Section for ICO images */
  ico,
  /** Section for JPG images */
  jpg,
  /** Section for PNG images */
  png,
  /** Section for PNM images */
  pnm,
  /** Section for PSD images */
  psd,
  /** Section for PVR images */
  pvr,
  /** Section for TIFF images */
  tiff,
  /** Section for TGA images */
  tga,
  /** Section for transformation operations */
  transform,
  /** Section for WEBP images */
  webp,
}
